package assignments_coreJava;

//10. Write a Java program to define two integer variables "a" and "b", assign the values 10 and 20 respectively. Find out the smaller number and display.

public class Qn10Smaller {

	public static void main(String[] args) {
		int a = 10, b = 20;

		if (a < b)
			System.out.println("a is smaller");
		else
			System.out.println("b is smaller");
	}

}
